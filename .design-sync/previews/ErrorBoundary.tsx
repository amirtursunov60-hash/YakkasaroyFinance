// Превью ErrorBoundary — граница ошибок: ловит исключение рендера в поддереве
// и показывает понятный экран вместо «белого экрана смерти».
// Чтобы показать именно ФОЛЛБЭК (а не проброс детей), внутрь кладём компонент,
// который бросает при рендере — boundary его перехватывает и рисует экран ошибки.
import { ErrorBoundary } from "yakkasaroy-management";

function Boom(): null {
  // Реалистичная доменная ошибка финконтура.
  throw new Error(
    "Не удалось загрузить Реестр fp_register: фонд ФД4 — баланс ушёл бы в минус (запрет на уровне БД)."
  );
}

// Сработавшая граница — отрисован экран ошибки с текстом и кнопкой «Перезагрузить».
export function Fallback() {
  return (
    <ErrorBoundary>
      <Boom />
    </ErrorBoundary>
  );
}
