-- Заявки §7: статус «отозвана» (withdrawn) ≠ «отклонена» (rejected).
-- Заявитель может отозвать свою ЗРС, пока она на рассмотрении (submitted) —
-- это его собственное действие, а не решение финкомитета. Отдельный статус
-- нужен, чтобы в отчётах/фильтрах отличать добровольный отзыв от отказа.
--
-- ВАЖНО (PostgreSQL): новое значение enum нельзя использовать в той же
-- транзакции, где оно добавлено. Поэтому ADD VALUE вынесено в отдельную
-- миграцию, а функция/логика, опирающаяся на 'withdrawn' — в следующую
-- (20260625000100_fp_withdraw_request.sql).
--
-- request_status — общий enum для payment_requests, supplier_bills и
-- payroll_sheets. Добавление значения безопасно: существующие строки и
-- политики не затрагиваются, 'withdrawn' просто становится доступным.

alter type public.request_status add value if not exists 'withdrawn';
